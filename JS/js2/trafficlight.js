let light=prompt("Enter the colour of traffic light: ");
if(light==="red"||light==="Red"){
    alert("Stop, light turned red");
}
else if(light==="yellow"||light==="Yellow"){
    alert("Wait, light turned yellow");
}
else if(light==="green"||light==="Green"){
    alert("go, light turned green");
}
else{
    alert("invalid light");
}